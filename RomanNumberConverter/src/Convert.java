
public class Convert {

	public static void convert(int number) {
		switch (number) {
		case 1:
			System.out.print("I");
			break;
		case 2:
			System.out.print("II");
			break;
		case 3:
			System.out.print("III");
			break;
		case 4:
			System.out.print("IV");
			break;
		case 5:
			System.out.print("V");
			break;
		case 6:
			System.out.print("VI");
			break;
		case 7:
			System.out.print("VII");
			break;
		case 8:
			System.out.print("VIII");
			break;
		case 9:
			System.out.print("IX");
			break;
		case 10:
			System.out.print("X");
			break;
		case 40:
			System.out.print("CL");
			break;
		case 50:
			System.out.print("L");
			break;
		case 90:
			System.out.print("XC");
			break;
		case 100:
			System.out.print("C");
			break;
		case 400:
			System.out.print("CD");
			break;
		case 500:
			System.out.print("D");
			break;
		case 900:
			System.out.print("CM");
			break;
		case 1000:
			System.out.print("M");
			break;
		}
	}

}
